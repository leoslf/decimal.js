if (typeof T === 'undefined') require('../setup');

T('radians', function () {
  Decimal.config({
    precision: Decimal.MAX_DIGITS,
    rounding: Decimal.ROUND_HALF_UP,
  });

  var t = function (expected, degrees) {
    T.assertEqualDecimal(new Decimal(expected), new Decimal(degrees).radians());
  }

  t(0, 0);
  // t(Decimal.pi(0.5), 90);
  t("1.570796326794896619231321691639751442098584699687552910487472296153908203143104499314017412671058534e+0", 90);
  // t(Decimal.pi(1), 180);
  t("3.141592653589793238462643383279502884197169399375105820974944592307816406286208998628034825342117068e+0", 180);
  // t(Decimal.pi(1.5), 270);
  t("4.712388980384689857693965074919254326295754099062658731462416888461724609429313497942052238013175602e+0", 270);
  // t(Decimal.pi(2), 360);
  t("6.283185307179586476925286766559005768394338798750211641949889184615632812572417997256069650684234136e+0", 360);
});
